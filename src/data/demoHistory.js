export function getProgressionData() {
  return DEMO_HISTORY.map(session => ({
    label: session.label.split('—')[0].trim(),
    date: new Date(session.savedAt).toLocaleDateString('es-MX', { day: 'numeric', month: 'short' }),
    rom: session.summary.meanROM,
    fatigue: session.summary.fatigueIndexROM,
    cv: session.summary.cvROM,
    tut: session.summary.meanTUT,
    trunk: session.summary.meanTrunkCompensation,
    reps: session.totalReps,
    ceRatio: session.summary.meanCERatio,
  }));
}
